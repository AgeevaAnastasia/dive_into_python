class Singleton:
    _instance = None
    def __new__(cls, *args, **kwargs):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
        return cls._instance
    def __init__(self, name: str):
        self.name = name


a = Singleton('–ü–µ—Ä–≤—ã–π')
print(f'{a.name = }')
b = Singleton('–í—Ç–æ—Ä–æ–π')
print(f'{a is b = }')
print(f'{a.name = }\n{b.name = }')


